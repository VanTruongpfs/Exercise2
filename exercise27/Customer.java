package exercise27;

public class Customer {
	private String name;
	private double charges;
/**
 * this is a constructor of class Customer
 * 
 * @param name
 * @param charges
 * example
 *  new Customer("Truong",500)
 *  new Customer("Long",1000)
 *  new Customer("Anh",500)
 */
	public Customer(String name, double charges) {
		this.name = name;
		this.charges = charges;
	}
	

}
